public class ClassMath {
    public static void main(String [] args) {

        //System.out.println(Math.PI);
        //System.out.println(Math.E);


        double result;

        result = Math.pow(2,3); //pow = Elevado (2 elevado a 3)
        result = Math.abs(-5.22);  //
        result = Math.sqrt(25); //sqrt = raíz cuadrada
        result = Math.round(3.145); //round = para arredondar (3.145 arredonda para 3, enquanto 3.5 arredonda para 4)
        result = Math.ceil(3.145); //ceil = para arredondar para cima (3.145 arredonda para 4, enquanto 3.5 arredonda para 4)
        result = Math.floor(3.99); //floor = para arredondar para baixo (3.145 arredonda para 3, enquanto 3.5 arredonda para 3)
        result = Math.max(5, 10); //max = para encontrar o maior valor entre dois números (5 e 10, nesse caso, retorna 10)
        result = Math.min(5, 10); //min = para encontrar o menor valor entre dois números (5 e 10, nesse caso, retorna 5)
        

        
        System.out.println(result);
       
      }
     }